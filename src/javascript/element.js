window.addEventListener("load", async () => {
  if (typeof window.ethereum !== 'undefined') {
    window.web3 = new Web3(window.ethereum);
    try {
      await window.ethereum.enable();
    } catch (error) {
      console.error("User denied account access");
    }
  } else {
    console.error("Web3 provider not found. Consider installing MetaMask.");
  }
    accounts = await window.ethereum.request({ method: 'eth_accounts' });
  contract = new window.web3.eth.Contract(Content_LIST_ABI, Content_LIST_ADDRESS);
  secondContract = new window.web3.eth.Contract(ABI, Address);
  console.log(secondContract)
  var length = await contract.methods.getIdeaCount().call()
//   alert(length)
  var data = `<tr>
  <th>Name</th>
  <th>Idea Topic</th>
  <th>Project Brief</th>
  <th>Budget</th>
  <th>Reward</th>
</tr>`;
  for (let index = 0; index < length; index++) {
    const idea = await contract.methods.ideas(index).call()
    console.log(idea)
    data += `
            <tr>
              <td id="name" name="name">${idea.name}</td>
              <td id="idea-name" name="name">${idea.topic}</td>
              <td id="idea-description" name="idea-description">${idea.description}</td>
              <td id="budget" name="budget">${idea.budget}</td>
              <td><button onclick = "RewardIdea('${idea.owner}')">Reward</button></td>
            </tr> 
    `
  }
  console.log(data)
  document.getElementById("ideaTableBody").innerHTML = data;
});


